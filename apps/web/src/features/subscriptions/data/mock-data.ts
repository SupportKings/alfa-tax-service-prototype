export function getTotalActiveRevenue(): number {
	return mockSubscriptions
		.filter(
			(s) => s.status === "Active - Current" || s.status === "Active - Behind",
		)
		.reduce((sum, s) => {
			if (s.frequency === "Monthly") return sum + s.price;
			if (s.frequency === "Quarterly") return sum + s.price / 3;
			if (s.frequency === "Annually") return sum + s.price / 12;
			return sum;
		}, 0);
}
