export function getStatusDistribution(): StatusDistributionData[] {
	const counts: Record<string, number> = {};
	for (const sub of mockSubscriptions) {
		counts[sub.status] = (counts[sub.status] || 0) + 1;
	}
	const colorMap: Record<string, string> = {
		"Active - Current": "hsl(142, 71%, 45%)",
		"Active - Behind": "hsl(0, 84%, 60%)",
		"Pending Setup": "hsl(217, 91%, 60%)",
		Paused: "hsl(45, 93%, 47%)",
		Cancelled: "hsl(0, 0%, 60%)",
		Completed: "hsl(0, 0%, 45%)",
	};
	return Object.entries(counts).map(([status, count]) => ({
		status,
		count,
		color: colorMap[status] || "hsl(0, 0%, 50%)",
	}));
}

